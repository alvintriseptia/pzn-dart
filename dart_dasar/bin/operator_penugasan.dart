void main() {
  double a = 10;
  //add
  a += 10;
  print(a);
  //sub
  a -= 10;
  print(a);
  //mul
  a *= 10;
  print(a);
  //div
  a /= 10;
  print(a);
  //mod
  a %= 10;
  print(a);
  //increment
  a++; // i = i+1
  print(a);

  double b = ++a; // b = i+1
  print(b);

  //decrement
  a--;
  print(a);
}
